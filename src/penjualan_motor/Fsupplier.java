    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
try {
     
        int row =jTable1.getSelectedRow();
        String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
        java.sql.Connection conn =(java.sql.Connection)penjualan_motor.koneksi.configDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet sql = stm.executeQuery("select * from tbsupplier where id_supplier='"+tabel_klik+"'");
        if(sql.next()){
            String id_supplier = sql.getString("id_supplier");
            jTextField1.setText(id_supplier);
            String nama=sql.getString("nama");
            jTextField2.setText(nama);
            String alamat = sql.getString("alamat");
            jTextField3.setText(alamat);
            String hp = sql.getString("hp");
            jTextField4.setText(hp);          
           
        }
    } catch (SQLException e) {}          // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

