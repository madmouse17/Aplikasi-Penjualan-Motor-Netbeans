      private void otomatis()
    {
       try {
            String sql ="select * from tbsupplier order by id_supplier desc";
            java.sql.Connection conn = (java.sql.Connection)penjualan_motor.koneksi.configDB();
            st= conn.createStatement();
            rs=st.executeQuery(sql);
            if (rs.next()) {
                String id = rs.getString("id_supplier").substring(3);
                String AN = "" + (Integer.parseInt(id) + 1);
                String Nol = "";
    
               if(AN.length()==1)
                {Nol = "0";}
                else if(AN.length()==2)
                {Nol = "";}

               jTextField1.setText("SUP" + Nol + AN);
            } else {
               jTextField1.setText("SUP01");
            }

           }catch(SQLException | NumberFormatException e){
           JOptionPane.showMessageDialog(null, e);
           }
     }
